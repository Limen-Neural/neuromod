//! # Classical Hebbian Learning (Pure STDP)
//! 
//! Honors Donald O. Hebb's 1949 principle: "Neurons that fire together wire together."
//! "When an axon of cell A is near enough to excite a cell B and repeatedly or persistently takes part in firing it,
//! some growth process or metabolic change takes place in one or both cells such that A's efficiency, as one of the cells firing B, is increased."
//! 
//! This module implements a pure (unmodulated) Spike-Timing-Dependent Plasticity (STDP) learning rule, which is a biologically inspired mechanism
//! for synaptic weight updates based on the precise timing of pre- and post-synaptic spikes.

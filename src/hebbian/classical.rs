/// Minimal Q88 fixed-point wrapper (stub for compilation)
#[derive(Clone, Copy, Debug)]
pub struct Q88(f32);
impl Q88 {
    pub fn from(v: f32) -> Self { Q88(v) }
    pub fn to_f32(&self) -> f32 { self.0 }
}

#[derive(Debug, Clone, Copy)]
pub struct StdpParams {
    pub a_plus: f32, // Maximum potentiation (LTP) amplitude
    pub a_minus: f32, // Maximum depression (LTD) amplitude
    pub tau_plus: f32, // Time constant for LTP (ms / steps)
    pub tau_minus: f32, // Time constant for LTD (ms / steps)
    pub w_min: f32, // Minimum synaptic weight
    pub w_max: f32, // Maximum synaptic weight
}

impl Default for StdpParams {
    fn default() -> Self {
        Self {
            a_plus: 0.01, // Typical LTP amplitude
            a_minus: 0.012, // Slightly stronger LTD for stability
            tau_plus: 20.0, // Typical LTP time constant (ms / steps)
            tau_minus: 20.0, // Typical LTD time constant (ms / steps)
            w_min: 0.0, // No negative weights (no inhibitory synapses in this simple model)
            w_max: 2.0, // Maximum weight to prevent runaway excitation
        }
    }
}

/// Pure Hebbian STDP update function (no reward/neuromodulator multiplayer)
/// This function calculates the weight change based on the timing of pre- and post-synaptic spikes.
pub fn apply_classical_stdp(pre_spike_time: i64, post_spike_time: i64, current_weight: f32, params: &StdpParams) -> f32 { // Calculate the time difference between pre and post spikes
    let delta_t = post_spike_time - pre_spike_time; // Time difference in ms / steps
    let weight_change = if delta_t > 0 {
        // Pre before post → LTP
        params.a_plus * (-delta_t as f32 / params.tau_plus).exp()
    } else if delta_t < 0 {
        // Post before pre → LTD
        -params.a_minus * (delta_t as f32 / params.tau_minus).exp()
    } else {
        0.0 // No change if they spike at the same time
    };
    
    // Update weight and clip to bounds
    let new_weight = (current_weight + weight_change).clamp(params.w_min, params.w_max);
    new_weight
}

